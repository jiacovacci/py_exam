# DATA OPERATOR EXAMPLES ##########

### Arithmetic Operators ###
### ADD ###
x = 2 + 3
print('ADD x = 2 + 3')
print(x)

### SUBTRACT ###
print('SUBTRACT x = 3 -2')
x = 3 -2
print(x)

### MULTIPLY ###
print('MULTIPLY x = 3 * 2')
x = 3 * 2
print(x)

### DIVISION ###
print('DIVIDE x = 3 / 2')
x = 3 / 2

### MODULUS ###
print('MODULUS x = 5 % 2')
x = 5 % 2
print(x)

### EXPONENTIAL ###
print('EXPONENTIAL x = 3**2')
x = 3**2
print(x)

